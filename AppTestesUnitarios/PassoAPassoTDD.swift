/*
ATIVIDADE
Passo a passo TDD



 1. Escreva um teste: Antes de escrever qualquer código, escreva um ou mais testes que descrevam o comportamento esperado do código. Esses testes devem falhar inicialmente, pois o código ainda não foi escrito.

 2. Execute os testes: Execute os testes escritos anteriormente para garantir que eles falhem como
 esperado.

 3. Escreva o código: Escreva o código necessário para fazer os testes passarem. O objetivo é escrever o menor código possível para fazer os testes passarem.

 4. Execute os testes novamente: Execute os testes novamente para garantir que eles passem.

 5. Refatorar o código: Se necessário, refatore o código para torná-lo mais limpo e legível, sem mudar o comportamento esperado.

 Repita os passos 1-5 para cada nova funcionalidade ou correção de bug.
 É importante notar que o TDD é uma abordagem iterativa, por isso, é necessário continuar a escrever testes para cobrir todas as funcionalidades e refatore o código à medida que novos requisitos são adicionados.


 */
